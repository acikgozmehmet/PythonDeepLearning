first_number = int(input("Enter First Integer Number : "))
second_number = int(input("Enter Second Integer Number : "))

print("Addition x + y : " + str(first_number + second_number))
print("Subtraction x - y : " + str(first_number - second_number))
print("Multiplication x * y : " + str(first_number * second_number))
print("Division (float) x / y : " + str(first_number / second_number))
print("Division (floor) x // y : " + str(first_number // second_number))
print("Modulus x % y : " + str(first_number % second_number))
print("Power x ** y : " + str(first_number ** second_number))
print("Power y ** x : " + str(first_number ** second_number))

